package locigLesson;

public class VurmaCedveli {
    public static void main(String[] args) {
        int num = 14;
        int sum = 0;
        while (sum <= 10) {
            int result = num * sum;
            System.out.println(num + " * " + sum + " = " + result);
            sum++;
        }
    }
}
